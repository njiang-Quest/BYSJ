package com.bysj.bean;

public class SummaryBean {
	private String option;
	private String kaopingren;
	private int score;
	public String getOption() {
		return option;
	}
	public void setOption(String option) {
		this.option = option;
	}
	public String getKaopingren() {
		return kaopingren;
	}
	public void setKaopingren(String kaopingren) {
		this.kaopingren = kaopingren;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}

}
